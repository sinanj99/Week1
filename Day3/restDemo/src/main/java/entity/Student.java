package entity;

/**
 *
 * @author sinanjasar
 */
public class Student {
    private int age;
    private String name;

    public Student() {
    }

    
    public Student(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }
    
    
}
